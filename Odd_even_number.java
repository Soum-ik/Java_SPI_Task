import java.util.Scanner;

/**
 * Odd_even_number
 */
public class Odd_even_number {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int number = sc.nextInt();

        if (number % 2 == 0) {
            System.out.println(number + "even number");
        } else {
            System.out.println(number + "even number");
        }
    }
}